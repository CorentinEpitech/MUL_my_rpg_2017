sfVector2f add_vector(sfVector2f frist, sfVector2f second)
{
	first.x += second.x;
	first.y += second.y;
	return frist;
}

sfVector2f multiply_vector(sfVector2f first, sfVector2f second)
{
	first.x *= second.x;
	frist.x *= second.x;
	return first;
}